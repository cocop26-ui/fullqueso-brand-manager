def render_dashboard():
    st.header("üìä Dashboard Principal")
    
    col1, col2, col3, col4 = st.columns(4)
    
    # Cargar datos
    productos = load_json_data("data/productos/productos.json")
    briefs = load_json_data("data/briefs/briefs.json")
    promociones = load_json_data("data/promociones/promociones.json")
    
    with col1:
        st.metric("üõçÔ∏è Productos", len(productos))
        productos_activos = len([p for p in productos if p.get("estado") == "Activo"])
        st.write(f"‚úÖ {productos_activos} activos")
        
    with col2:
        st.metric("üìã Briefs", len(briefs))
        if briefs:
            st.write(f"üìÖ √öltimo: {briefs[-1]['nombre_campana']}")
        
    with col3:
        st.metric("üéâ Promociones", len(promociones))
        promos_activas = len([p for p in promociones if p.get("activa", False)])
        st.write(f"‚úÖ {promos_activas} activas")
        
    with col4:
        # Calcular valor total de productos
        valor_total = sum([p.get("precio", 0) for p in productos])
        st.metric("üí∞ Valor Inventario", f"${valor_total:,.2f}")
    
    st.divider()
    
    # Resumen reciente
    if productos or briefs or promociones:
        st.subheader("üìà Actividad Reciente")
        
        col_recent1, col_recent2 = st.columns(2)
        
        with col_recent1:
            if productos:
                st.write("**üõçÔ∏è √öltimos Productos:**")
                for prod in productos[-3:]:
                    st.write(f"‚Ä¢ {prod['nombre']} - ${prod['precio']}")
        
        with col_recent2:
            if promociones:
                st.write("**üéâ Promociones Activas:**")
                promos_activas = [p for p in promociones if p.get("activa", False)]
                for promo in promos_activas[-3:]:
                    st.write(f"‚Ä¢ {promo['nombre']} - {promo['codigo_cupon']}")

